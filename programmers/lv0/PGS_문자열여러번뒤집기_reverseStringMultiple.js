function solution(my_string, queries) {
  // 1. ë¬¸ìì—´ì„ ìˆ˜ì • ê°€ëŠ¥í•œ ë°°ì—´ë¡œ ë³€í™˜
  let strArr = [...my_string];

  // 2. ê° ì¿¼ë¦¬ì— ëŒ€í•´ ë°˜ë³µ ìˆ˜í–‰
  queries.forEach(([s, e]) => {
    // êµ¬ê°„ [s, e]ì˜ ê¸€ìë“¤ì„ ì¶”ì¶œ
    const target = strArr.slice(s, e + 1);
    // ë’¤ì§‘ê¸°
    target.reverse();
    // ì›ë³¸ ë°°ì—´ì˜ í•´ë‹¹ êµ¬ê°„ì„ ë’¤ì§‘íŒ ê¸€ìë“¤ë¡œ êµì²´ (splice)
    strArr.splice(s, target.length, ...target);
  });

  return strArr.join('');
}

