function solution1(my_string, queries) {
  var answer = '';

  for (let i of queries) {
    my_string =
      my_string.substring(0, i[0]) +
      my_string
        .substring(i[0], i[1] + 1)
        .split('')
        .reverse()
        .join('') +
      my_string.substring(i[1] + 1);
  }

  answer = my_string;
  return answer;
}

